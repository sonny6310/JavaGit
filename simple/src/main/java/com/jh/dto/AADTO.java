package com.jh.dto;

public class AADTO {
	private int aa;
	private String bb;

	public int getAa() {
		return aa;
	}

	public void setAa(int aa) {
		this.aa = aa;
	}

	public String getBb() {
		return bb;
	}

	public void setBb(String bb) {
		this.bb = bb;
	}

	@Override
	public String toString() {
		return "AADTO [aa=" + aa + ", bb=" + bb + "]";
	}
}
